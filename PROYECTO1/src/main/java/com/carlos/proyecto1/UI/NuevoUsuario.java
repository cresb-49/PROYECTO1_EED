    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        String userName = jTextField1.getText();
        if (userName.isBlank()) {
            JOptionPane.showMessageDialog(this, "Debe de escribir un nombre de usuario");
        } else {
            if (this.verificarUsuario(userName)) {
                try {
                    datosP.getUsuarios().agregar(new NodoArbol(userName, new usuario(userName, new ListaDobleEnlazada())));
                    JOptionPane.showMessageDialog(this, "Se ingreso con exito el usuario \""+userName+"\" al sistema\npuede asignarle imagenes en el apartado de esta misma opcion");
                } catch (CloneNodeException ex) {
                    JOptionPane.showMessageDialog(this, "Ya existe un usuario \""+userName+"\" en el sistema");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Los usuarios solo deben tener caracteres alafanumericos");
            }
        }

    }//GEN-LAST:event_btnIngresarActionPerformed


