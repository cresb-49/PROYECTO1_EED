    private boolean verificarUsuario(String userName) {
        Pattern pattern = Pattern.compile("^[0-9]*[a-zA-Z]+([0-9]|[a-zA-Z])*$");
        Matcher matcher = pattern.matcher(userName);
        boolean matchFound = matcher.find();
        if (matchFound) {
            return true;
        } else {
            return false;
        }
    }
